<template>
  <div>
    <el-breadcrumb separator="/">
      <el-breadcrumb-item v-for="(title, index) in breadCrumb" :key="index">{{
        title
      }}</el-breadcrumb-item>
    </el-breadcrumb>
  </div>
</template>

<script>
export default {
  data() {
    return {
      breadCrumb: [],
    };
  },
  components: {},
  computed: {},
  methods: {
    getBreadcrumb() {
      let title = this.$route.meta.title;
      if (title[0] !== "扶뒬못") {
        title.unshift("扶뒬못");
      }
      this.breadCrumb = title;
    },
  },
  created() {
    this.getBreadcrumb();
  },
};
</script>
